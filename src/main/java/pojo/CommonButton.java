package pojo;

/**
 * 普通按钮（子按钮）
 *
 * Created by Mr丶周 on 2017/3/19.
 */
public class CommonButton extends Button {
	private String type;
	private String key;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}
}